# dict : 
'''
In Python, 
a dictionary is a built-in data structure that stores data in key-value pairs. 
It is an unordered collection, meaning the order of items is not guaranteed. 
Dictionaries are mutable, allowing for changes after creation.

Key Features:
Keys: Must be unique and immutable (e.g., strings, numbers, tuples).
Values: Can be any data type (e.g., strings, numbers, lists, other dictionaries).
Syntax: Created using curly braces {} or the dict() constructor.
'''
